#include <unistd.h>

void	ft_putnbr(int n)
{
	char tmp;

	if (n == -2147483648)
	{
		write(1, "-214748364", 10);
		n = 8;
	}
	if (n < 0)
	{
		write(1, "-", 1);
		n = -n;
	}
	if (n / 10 > 0)
		ft_putnbr(n / 10);
	tmp = n % 10 + 48;
	write(1, &tmp, 1);
}
