#include "libft.h"

int		ft_sqrt(int nb)
{
	int k;

	k = 1;
	if (nb <= 0)
		return (0);
	else
	{
		while (k <= nb / k)
		{
			if (k == nb / k && nb % k == 0)
				return (k);
			else
				++k;
		}
	}
	return (0);
}
