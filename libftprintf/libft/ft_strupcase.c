#include "libft.h"

char	*ft_strupcase(char *str)
{
	int k;

	k = 0;
	while (str[k] != '\0')
	{
		if (str[k] >= 97 && str[k] <= 122)
		{
			str[k] = str[k] - 32;
			++k;
		}
		else
			++k;
	}
	return (str);
}
