#include <unistd.h>

void	ft_putnbr_fd(int n, int fd)
{
	char tmp;

	if (n == -2147483648)
	{
		n = 8;
		write(fd, "-214748364", 10);
	}
	if (n < 0)
	{
		n = -n;
		write(fd, "-", 1);
	}
	if (n / 10 > 0)
		ft_putnbr_fd(n / 10, fd);
	tmp = n % 10 + 48;
	write(fd, &tmp, 1);
}
