#include "libft.h"

void	ft_deltab(char ***tab)
{
	int		i;

	if (tab)
	{
		i = 0;
		while ((*tab)[i])
		{
			ft_strdel(&((*tab)[i]));
			i++;
		}
		ft_strdel(&((*tab)[i]));
		free(*tab);
		*tab = NULL;
	}
}
